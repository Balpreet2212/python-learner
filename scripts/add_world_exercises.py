"""Add world-specific exercises to all 12 missing lesson YAML files."""
import yaml, os

base = 'c:/Users/pyrot/Documents/Coding/python-learner/content/units'

def add_worlds(path, worlds_content):
    with open(path, encoding='utf-8') as f:
        data = yaml.safe_load(f)
    for world_key, content in worlds_content.items():
        if world_key not in data.get('worlds', {}):
            data.setdefault('worlds', {})[world_key] = {}
        data['worlds'][world_key].update(content)
    with open(path, 'w', encoding='utf-8', newline='\n') as f:
        yaml.dump(data, f, default_flow_style=False, allow_unicode=True,
                  sort_keys=False, width=120)
    print(f'Updated {path}')


